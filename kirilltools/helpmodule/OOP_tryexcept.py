class Pet: # наш основной класс
    def __init__(self, name, age): # в прошлом примере я просто показал new, а так он сам есть
        # назначаем СТРОГО НАСТРОГО SELF-У! self это обьект внутри класса. снаружи self меняется на название обьекта а точней переменной
        self.name = name
        self.age = age 

