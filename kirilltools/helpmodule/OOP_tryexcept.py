    def __init__(self, name, age):
        # Pet.__init__(self, name, age) # такое обычно не используют. надо через super (да ту самую вещь от которой я брал __new__)
        super().__init__(self, name, age) # кстати расскажу интересное. super дает доступ к классу который наследуем! мы используем его метод __init__! а почему не назначается несуществуещему обьекту pet? потому-что мы передали СВОЙ ОБЬЕКТ! СВОЙ SELF

